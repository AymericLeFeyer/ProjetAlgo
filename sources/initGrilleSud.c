//retourne le nombre d'erreurs
int verification(JoueurSudoku g)
{
  int v = 0;
  for (int i = 0; i < 9; i++)
  {
    for (int j = 0; j < 9; j++)
    {
      if (g.g.tab[i][j] != g.s.tab[i][j])
        v++;
    }
  }

  return v;
}
