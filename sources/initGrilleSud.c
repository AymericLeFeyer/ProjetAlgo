//v=0 si la grille est fausse 1 si bon, cf= compteur de fautes
int verification (grilleSudokuBrute g){
  int i,v,cf=0;
  for(i=0;i<=81;i++){
    if(g.gsb[i]!=g.gss[i]){
      cf++;
    }
  }
  if (cf>=0){
    v=0;
    printf("%d Erreur : ",cf);
  }
  else{
    v=1;
  }
  return v;
}
